            <p class="color-sky-blue  text-bold">GETTING HERE BY CAR</p>

            <p class="text-justify">
            From Manila, take the North Luzon Expressway. You may take the San Fernando exit and turn to Olongapo-Gapan Road through the towns of Lubao, Layak, Dinalupihan, and Roosevelt. Turn left at the Subic Bay Expressway/Tipo Road to enter the Freeport and then follow the signs to the Auction Site.
            </p>
            <br>
            <p class="text-justify">
            You may also take the Subic-Clark-Tarlac Expressway (SCTEX). After taking the NLEX San Fernando	exit, take the Spur/NLEX interchange located between the Dau and Sta. Ines exits and proceed to the SCTEX Clark Logistic Toll Plaza. After crossing	the bridge,follow the loop of the ramp towards Subic. It is approximately a straight one (1)-hour drive to the Freeport tollgate. Upon reaching the	tollgate, you may then follow the signs to the Auction Site. Free Shuttle Service shall be available to take you from the various hotels inside the Freeport to the auction site during the auction days.
            </p>
            <br>
            <p class="color-sky-blue text-bold">
            GETTING HERE BY PLANE
            </p>
            <p class="text-justify">
            Air travel to Manila
            The Ninoy Aquino International Airport is served by major international airlines such as Northwest, Cathay Pacific, Singapore, Japan, China, British Airlines, Thai Airways, EVA Air, Emirates, and Philippine Airlines among others. Local flight at the domestic airport are carried out by Cebu Pacific, Zest Airways, and Air Philippines.
            </p>
            <br>
            <p class="text-justify">
            Air travel through Clark Airport
            The Diosdado Macapagal International Airport serves both international and local flights (bound to and from Malaysia, Korea, Singapore, Macau, Hong Kong, Thailand, Cebu, and Aklan). Airlines serving said airport include Cebu Pacific, Tiger Airways, Sea Air, Asiana, and Air Asia, For more information about there flight schedules, please log on to <a href="htp://www.clarkairport.com" target="_blank"> htp://www.clarkairport.com</a>.
            </p>
