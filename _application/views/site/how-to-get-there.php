            <p class="color-sky-blue text-bold">
                GETTING HERE VIA VICTORY LINER
            </p>
            <p class="text-justify">Main stations from Manila are Pasay (Edsa, Pasay), Caloocan (713 Rizal Avenue Extension, Caloocan City) and Cubao (EDSA, Cubao). Buses' schedule (either Zambales or Olongapo) is from 4:00 AM with 1 hour interval.
            </p>
